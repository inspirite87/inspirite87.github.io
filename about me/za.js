function lovefunc(petalsTimmy, petalsSarah) {
    
    return (petalsTimmy % 2 !== petalsSarah % 2);
}


console.log(lovefunc(1, 4));
console.log(lovefunc(2, 2));
console.log(lovefunc(0, 1)); 
console.log(lovefunc(0, 0)); 



