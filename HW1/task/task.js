  function renderMovies() {
    const tableBody = document.querySelector("#moviesTable tbody");
    tableBody.innerHTML = "";
  
    movies.forEach((movie, index) => {
      const row = document.createElement("tr");
  
      const posterCell = document.createElement("td");
      const posterImg = document.createElement("img");
      posterImg.src = `https://image.tmdb.org/t/p/w500${movie.poster_path}`;
      posterImg.alt = movie.title;
      posterImg.className = "poster";
      posterCell.appendChild(posterImg);
  
      const titleCell = document.createElement("td");
      titleCell.textContent = movie.title;
  
      const releaseDateCell = document.createElement("td");
      releaseDateCell.textContent = movie.release_date;
  
      const voteAverageCell = document.createElement("td");
      voteAverageCell.textContent = movie.vote_average;
  
      const detailsCell = document.createElement("td");
      const detailsButton = document.createElement("button");
      detailsButton.textContent = "Details";
      detailsButton.dataset.index = index;
      detailsButton.addEventListener("click", showMovieDetails);
      detailsCell.appendChild(detailsButton);
  
      const favoritesCell = document.createElement("td");
      const favoritesButton = document.createElement("button");
      favoritesButton.textContent = isFavorite(movie) ? "Remove from Favorites" : "Add to Favorites";
      favoritesButton.dataset.index = index;
      favoritesButton.addEventListener("click", toggleFavorite);
      favoritesCell.appendChild(favoritesButton);
  
      row.appendChild(posterCell);
      row.appendChild(titleCell);
      row.appendChild(releaseDateCell);
      row.appendChild(voteAverageCell);
      row.appendChild(detailsCell);
      row.appendChild(favoritesCell);
  
      tableBody.appendChild(row);
    });
  }
  
  function showMovieDetails(event) {
    const index = event.target.dataset.index;
    const movie = movies[index];
    document.getElementById("modalTitle").textContent = movie.title;
    document.getElementById("modalPoster").src = `https://image.tmdb.org/t/p/w500${movie.poster_path}`;
    document.getElementById("modalOverview").textContent = `Overview: ${movie.overview}`;
    document.getElementById("modalReleaseDate").textContent = `Release Date: ${movie.release_date}`;
    document.getElementById("modalVoteAverage").textContent = `Vote Average: ${movie.vote_average}`;
    document.getElementById("modalLanguage").textContent = `Original Language: ${movie.original_language}`;
  
    document.getElementById("movieModal").style.display = "block";
  }
  
  function closeModal() {
    document.getElementById("movieModal").style.display = "none";
  }
  
  function toggleFavorite(event) {
    const index = event.target.dataset.index;
    const movie = movies[index];
    if (favorites.has(movie.id)) {
      favorites.delete(movie.id);
    } else {
      favorites.add(movie.id);
    }
    updateFavoritesList();
  }
  
  function updateFavoritesList() {
    const favoritesList = document.getElementById("favoritesList");
    favoritesList.innerHTML = "";
  
    movies.forEach((movie) => {
      if (favorites.has(movie.id)) {
        const listItem = document.createElement("li");
        listItem.textContent = movie.title;
  
        const deleteButton = document.createElement("button");
        deleteButton.textContent = "Delete";
        deleteButton.dataset.id = movie.id;
        deleteButton.addEventListener("click", deleteFromFavorites);
  
        listItem.appendChild(deleteButton);
        favoritesList.appendChild(listItem);
      }
    });
  
   
  }
  
  function deleteFromFavorites(event) {
    const movieId = parseInt(event.target.dataset.id, 10);
    favorites.delete(movieId);
    updateFavoritesList();
  }
  
  function isFavorite(movie) {
    return favorites.has(movie.id);
  }
  
  renderMovies();
  
  document.querySelector(".close").addEventListener("click", closeModal);
  window.addEventListener("click", (event) => {
    if (event.target === document.getElementById("movieModal")) {
      closeModal();
    }
  });


